// Parse month from German period string
function parseMonth(period: string): string {
  const months: Record<string, string> = {
    'januar': '01', 'februar': '02', 'märz': '03', 'april': '04',
    'mai': '05', 'juni': '06', 'juli': '07', 'august': '08',
    'september': '09', 'oktober': '10', 'november': '11', 'dezember': '12',
    'jan': '01', 'feb': '02', 'mär': '03', 'apr': '04',
    'aug': '08', 'sept': '09', 'okt': '10', 'nov': '11', 'dez': '12'
  }
  
  const periodLower = period.toLowerCase()
  for (const [name, num] of Object.entries(months)) {
    if (periodLower.includes(name)) return num
  }
  return '01'
}

// Parse year from period string
function parseYear(period: string): number {
  const match = period.match(/20\d{2}/)
  return match ? parseInt(match[0]) : 2012
}

